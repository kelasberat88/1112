      <p><span class="style1" id="intelliTXT">Custom 2004 Chevrolet Silverado</span></p>
      <p align="left" class="style2">Some performance  poseurs tout the virtues of a velvet glove on an iron fist. But don't most guys  just wanna go fast? No velvet - just raw, unrefined Detroit iron. It's a proud  tradition that goes back to the days of the '70 Chevelle SS 454 with the  &quot;stripe delete&quot; option. The lack of any body markings made this  popular musclecar a sleeper, a Q-ship that bared its gun barrels only when  challenged at the stoplight.<br />
In building  his all-red, supercharged, and intercooled '04 Chevy Silverado, Jim Bell of  Kenne Bell reached back to this era of no fluff, all-business performance. It  was a time when Chevy produced a whole slew of big V-8, rear-drivin' road cars  such as the Nova, Camaro, and Impala.</p>
      <p align="left" class="style2"><img src="images/0507st_05_z+2004_chevrolet_silverado+custom_seats.jpg" width="140" height="105" /></p>
      <p align="left" class="style2">Those days  are gone now, replaced with geeky front-wheel-drive techno platforms. OK, at  least there's the GTO, but it's built in Australia, for crying out loud, and  it's a Pontiac to boot. Where's a good rear-drivin', big-block Chevy SS when  you need one?</p>
      <p align="right" class="style2"><img src="images/0507st_07_z+2004_chevrolet_silverado+supercharged_engine.jpg" width="140" height="105" /></p>
      <p align="left" class="style2">Well, this  blown Silverado may not be a sedan on steroids, but it's sure one kick-butt  muscle truck. How 'bout 500 hp and 566 lb-ft of torque? And those figures were  measured at the wheels on a chassis, so actual engine horsepower is in the  neighborhood of 600-plus horses and 700 lb-ft of torque. These tests were done  on the intercooled version with 100-octane fuel, which allows the boost to go  as high as 13 psi. (For comparison, the stock 5.3L puts out 244 hp and 289  lb-ft at the wheels, according to Kenne Bell's data.)</p>
