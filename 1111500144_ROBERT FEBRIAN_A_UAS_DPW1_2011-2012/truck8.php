      <p><span class="style1" id="intelliTXT">Custom 1999 Chevrolet C3500</span></p>
      <p align="left" class="style2">It's a  somewhat rare occurrence these days when the tech sheet that we provide to  truck owners to tell us about themselves and their trucks reads funnier than  the owner's Myspace.com profile. But, such is the case with Jeff Davy, owner of  Devious Customs in Ontario, California. His tech sheet was full of witty  answers to questions like &quot;How many previous customs have you built?&quot;  His reply was, &quot;Which day?&quot; Another response that got a laugh here at  the office was his answer to the question: &quot;Were there any interesting  things that happened during the build?&quot; Jeff wrote that his daughter was  conceived in the back seat of this dualie and delivered in the front seat. He's  a character all right-easy going when it comes to life but serious as a heart  attack when it comes to building custom trucks.<br />
And so it  goes for a family man with one of the fastest-growing custom shops in Southern  California. Starting with practically nothing, his shop has grown to turn out a  ton of trophy-taking rides over the years, and it seems that even with four  kids and a wife all vying for his time, Jeff still manages to put something  cool together for himself. Call it boundless energy, if you will, but this guy  has built quite a few killer trucks, and most of them have been Chevy Crew Cab  dualies. His latest, a '99 Chevy C3500, is perhaps his best, even if it's not  finished.</p>
      <p align="left" class="style2"><img src="images/0707st_03_z+1999_chevrolet_c3500+rear_wheel_tubs.jpg" width="140" height="105" /><br />
