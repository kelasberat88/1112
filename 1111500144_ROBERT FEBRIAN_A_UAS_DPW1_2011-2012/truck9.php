      <p class="style2">Custom 1968 Chevrolet C10</p>
      <p align="left" class="style3">When you're  starting a project, you have to have passion for the vehicle. It's similar to  meeting and dating someone new. If there's no chemistry, what's the point in  pretending? Robert Wallace of Cleveland, North Carolina, found an old farm  truck that was on the fast track to a salvage yard. But, there was just  something about it that drew him in, chemistry. Sure, he paid little, but the  truck he desired was a late-'60s Chevy C10.</p>
      <p align="left" class="style3"><img src="images/0801st_06_z+1968_chevrolet_c10+custom_engine.jpg" width="140" height="105" /><br />
        Not long  after Robert bartered for the rusty Stepside, he dismantled the body panels  from the foundation. He replaced the front subframe with a '76 Chevy 1/2-ton  with disc brakes, Belltech 2-inch drop springs, Belltech 3-inch drop spindles,  and Speedway short shocks. Then, he narrowed the rear framerails, using  2x6-inch box tubing, to make way for the 21.5-inch-wide tires. Holding the rims  and tires in place is a coilover and ladder bar rear suspension setup that  holds the shortened Ford 9-inch rearend, stuffed with 4.56 Richmond gears. To  finish off the chassis, Robert secured the steering system, steering column,  and brake system from a '76 Chevy, as well.</p>
      <p align="left" class="style3"><img src="images/0801st_07_z+1968_chevrolet_c10+custom_interior.jpg" width="140" height="105" /><br />
        Moving on to  the motor, Robert realized he had no interest in renewing the straight-six  powerplant, so he picked up a 350 small-block and started by punching it out  0.030 over. Then, he packed it full with all sorts of goodies, such as Crane  solid lifters, rockers, and a two-grind bumpstick. But, the fun doesn't stop  there, because he also added forged rods and pistons, with the whole package  fired off by MSD. With a solid foundation, it was time to bolt on the Dyers  polished-aluminum intake manifold, with a polished-to-perfection Dyers 6-71  blower being fed by a pair of 750-cfm double-pumper carbs. Robert backed the  engine with an '85 700-R4, which he optimized with a TCI 3,000-stall converter  and trans cooler, controlled by a Hurst shifter.</p>
