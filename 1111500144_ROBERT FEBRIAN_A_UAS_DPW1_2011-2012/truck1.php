    <td><p align="center" class="style1"><span class="style4"><blink></blink></span><span class="style5"> 2007 Chevy Silverado - Fine In Five </span></p>
      <p class="style1">You've  probably had some serious thrash sessions over the years to get a truck ready  for a show, a race, or a long trip. Pulling all-nighters are not that big of a  deal, right? They are a rite of passage that any serious truck owner must  endure on the way to custom greatness. We thought we'd pulled off some  incredible feats of custom before, that is until we met Tahua Pena and found  out that he took a bone-stock '07 Chevy Silverado and completely transformed it  in just five nights</p>
      <p align="right" class="style1">.<img src="images/0912st_02_+2007_chevy_silverado+right_side_view1.jpg" width="140" height="105" /></p>
      <p class="style1"><br />
      The truck  you are looking at literally went from stock to full-blown custom in less than  five days. Tahua and a crew of five guys that work at his body shop, Dinuba  Collision, shut the doors at the end of each work day and then went ballistic  on his Chevy just so they could debut it at our Havoc show in Fresno with one  of the first '08 Caddy clip conversions. It's an improbable amount of work to  get done in five days, let alone just five nights. Although the drivetrain and  audio systems are stock, the miraculous story behind a truck he calls Pancho  Tequilas was enough for us to put it onto the cover.</p>
      <p class="style1"><img src="images/0912st_01_+2007_chevy_silverado+front_left_view22.jpg" width="100" height="75" /><br />
        <br />
    Tuesday  night rolled around, and once again the guys turned their attention to the  Chevy, this time cutting up the body with the help of employees Israel and  Tony. The taillights were shaved and replaced with Hi Tech LED Products  16-inch-long LED lights. The door handle holes were filled and contoured to fit  '07 Pontiac Grand Prix units. The rain gutters and third brake light were also  filled in. Given that the show was just three days away, you might think that  the guys might stop here and just get to sprayin' the new paint. Tahua wasn't  ready to quit though. He picked up a complete OEM '08 Cadillac Escalade front  clip and sliced, diced, and tweaked it until it looked like it belonged on the  truck. The guys also covered the holes left in the bed from the step notch and  massive wheels with fresh sheetmetal.</p>
