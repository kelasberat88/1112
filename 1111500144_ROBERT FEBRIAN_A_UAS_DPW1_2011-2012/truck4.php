      <p><span class="style1" id="intelliTXT">2003 Chevy Silverado Stepside</span></p>
      <p align="left" class="style2">When Art  Sanchez of Fresno, California, purchased this truck brand-new from the  dealership, he had no idea that it would go through a whirlwind of work that  ultimately turned it into the custom vehicle you see here. At the time, Art had  just graduated high school and scored himself employment at his father's  grocery store. He needed his own mode of transportation and was interested in  purchasing an affordable new vehicle that still offered some performance.  Because Art is over 6 feet tall and most sports cars are made small, he decided  to go with this more spacious Chevy Silverado.</p>
      <p align="left" class="style2"><img src="images/0903st_04_z+2003_chevy_silverado_stepside+custom_interior1.jpg" width="100" height="75" border="0" /></p>
      <p align="left" class="style2">Since there  are a lot of agriculture jobs in Fresno, there is an abundance of trucks on the  road in that particular region. Of course, when there are tons of people  driving generally the same vehicle, there are bound to be those of us who want  ours to be different. Art chose a standard-cab version of the truck with a Stepside  bed. Though it was already more distinctive, he wanted to make it look a little  better than stock. By treating his Silverado to a 2/4 drop and slapping on a  set of 22-inch wheels, Art had a truck that didn't look like all the others  roaming his hometown. Though it was very mild for a custom truck, this Chevy  was cleaner than most and Art was satisfied with it for the time being.</p>
      <p align="right" class="style2"><img src="images/0903st_09_z+2003_chevy_silverado_stepside+engine.jpg" width="100" height="75" /></p>
      <p align="left" class="style2"><br />
          <br />
        As he became more familiar with custom trucks, Art wanted to do more  things to his Chevy, like clean up the back end with a roll pan. To get a quote  on the job, he went to the Hot Rod Shop. Because it was a local place that did  high-quality work. Once he pulled in the parking lot, he couldn't help himself  from staring at Jay Martinez's wildly customized '99 Chevy Silverado. If you're  not familiar with the truck, it is slammed to the ground with airbags, has an  '02 Avalanche front end grafted-on, and graced the cover of this magazine  twice. It is one of those trucks that can grab anyone's attention, including  those who are not into highly modified trucks.</p>
