      <p class="style3">A lot of the  trucks on the road get by on their good looks, attracting attention with loud  paint, a hammered stance, and huge wheels. There's nothing wrong with that.  Lookin' good is half the battle. Though many of these rides are sporty, they  aren't true sport trucks. It's one thing to nail the stance when building a  truck, and you'll also score bonus points with us if you can pull off a paint  scheme that won't look dated in a couple of years, but if you want real kudos,  or a cover shot, then you've got to build the total package. A truck that runs  as good or better than it looks is what this mag' is all about, and merely  stuffing a big motor into your ride won't cut it. Paco Montalvo knows all too  well that building a balanced ride requires more from the builder than just  swapping engines and finding the right backspacing for a big set of wheels.<br />  
