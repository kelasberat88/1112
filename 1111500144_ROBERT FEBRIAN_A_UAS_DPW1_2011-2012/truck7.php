      <p><span class="style1" id="intelliTXT">Custom 2003 Chevrolet Silverado</span></p>
      <p align="left" class="style2">There are  millions of ways to get started building custom trucks. Some people are born  into it, sitting on the fender while dad works on his prized possession. Some  may find out about the world of custom trucks at other times in their lives.  Maybe one of your friends had a cool truck. Maybe there was a club in town that  had some impressive rides at the local cruise night. For 8-year-old Bryan  Stern, it was a local show spot that hooked him into the custom scene for life.  It was there that he began to admire and appreciate the things people did to </p>
      <p align="left" class="style2"><img src="images/0707st_02_z+2003_chevrolet_silverado+custom_audio_system.jpg" width="140" height="186" /></p>
      <p align="left" class="style2">their rides  to stand out from the next. Fast forward to present, and we find that Bryan  can't get enough. He is still playing with trucks, and his 8-to-5 is working  for Intro Wheels. He's also the owner of this clean '03 Chevy Silverado  standard cab.<br />
        Bryan's  philosophy of truck building is to start from the outside and work in. So, the  suspension was first on the list, where it was dealt with by the boys over at  GO-EZ Customs. Up front, the stock control arms were modified and combined with  a set of Slam Specialties airbags to get the front end down to Mother Earth,  while still allowing it enough lift to clear the massive wheels he was going to  throw at it. The rear end got a step-notch and a KP Components six-link that's  paired with a second set of Slam Specialties airbags, and a Watts link keeps  everything centered under the truck. For wheels, Bryan took a stroll through  the Intro warehouse and picked out a combination set of 22- and 24-inch Intro  Ram 6 wheels and wrapped them in Nitto rubber.</p>
      <p align="right" class="style2"><img src="images/0707st_03_z+2003_chevrolet_silverado+custom_seats.jpg" width="140" height="186" /></p>
      <p align="left" class="style2">Under the  hood, things were kept pretty simple. The 5.3L V-8 received a one-off K&amp;N  air intake with nitrous port that feeds into a Magna Charger Radix supercharger  with 6-1/2 pounds of boost. Of course, the exhaust was rerouted, and a JBA  Performance Exhaust system was added to accommodate the extra power and new  frame-laying stance. An Auburn locking posi was installed to get this newfound  power to the ground.</p>
