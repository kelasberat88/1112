    <td><h1 align="center" class="style1" id="ctl00_ctl10_ctl00_lblTitle"><span id="intelliTXT">1991 Chevy Silverado - Overlooked</span></h1>
      <p class="style2">There are  lots of trucks out there that get tons of attention. They sport mammoth wheels,  a huge body-drop, and so much custom paint that parking at the mall is no  longer an option. These trucks become less about function and more about style;  pretty showpieces that look good lying on the ground but then get hauled off on  a trailer instead of being driven. But sometimes, the really cool trucks slip  between the cracks. They don't get as much attention as their flashy brethren,  but the fact is they are better. That's the truck on these pages. Kelly  Dickinson, a sheetmetal worker from Sacramento, California, decided to build this  ride as a pretty showpiece, but with lots of little details that most people  miss at first glance. And it's those little details that make this truck such a  work of art once inspected further.</p>
      <span class="style2"><img src="images/0911st_03_+1991_chevy_silverado+door_panel1.jpg" width="140" height="105" /><br />
Let's take the bed for example, a part with tons of  sheetmetal mods that most wouldn't notice. The gas door has been converted to  an oval shape - a subtle mod for sure, something that people who drive an  '88-'98 CK might not notice. The back of the bed, right above the integrated  amp rack with 7-inch monitor, has been leveled out to match the bedsides.  That's because the stock wall of the bed sat below the rails, and Kelly wanted  it to look even. The corners of the bed by the tailgate have integrated spikes  with expanded metal inserts that flow around the gate and into the bedrail. To  match those details, the tailgate corners were radiused and smoothed, leaving a  tight gap that the factory never intended. That same tailgate      </span>
      <p class="style2">has the upper bodyline shaved as well as the stock handle. Below that, the  rollpan has an arch-shaped sheetmetal insert with more spike patterns with  expanded metal inserts, continuing the theme even further. Even the mirrors  sport a pair of pointed panels, jutting into the window area but looking even  sharper.</p>
<p align="center" class="style2"><img src="images/0911st_05_+1991_chevy_silverado+custom_interior2.jpg" width="140" height="105" /></p>
<p class="style2"><br />
  Under the  hood is even better. Most trucks nowadays have a pretty filter and a few billet  pieces here and there, but not this ride. Kelly went thefair.</p>
